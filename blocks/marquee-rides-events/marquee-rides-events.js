function startCarousel() {
    const waitForSwiper = (callback) => (function checkSwiper() { window.Swiper ? callback() : setTimeout(checkSwiper, 100); })();

    waitForSwiper(() => {
        // after here 

        new Swiper(".cmp--marquee-swiper", {
            direction: "horizontal",
            loop: !1,
            pagination: {
                el: ".swiper-pagination",
                clickable: !0
            },
            effect: "slide",
            slidesPerView: 1.2,
            spaceBetween: 20,
            breakpoints: {
                768: {
                    slidesPerView: 1.2
                },
                1024: {
                    slidesPerView: 3
                }
            }
        })
        // above here
    });
}
